// Save report preferences
export async function saveReportPreference(adminId, preferences) {
  await setDoc(doc(db, 'reportPreferences', adminId), {
    ...preferences,
    updatedAt: new Date().toISOString(),
  })
}

// Get report preferences
export async function getReportPreference(adminId) {
  const snap = await getDoc(doc(db, 'reportPreferences', adminId))
  return snap.exists() ? snap.data() : null
}
