import { collection, doc, setDoc, getDoc, getDocs, query, where } from 'firebase/firestore'
import { db } from '../firebase'

// Generate daily report data
export async function generateDailyReport(date = new Date()) {
  const dateStr = date.toISOString().split('T')[0]
  const [adsSnap, vehiclesSnap] = await Promise.all([
    getDocs(collection(db, 'ads')),
    getDocs(collection(db, 'vehicles')),
  ])
  const ads = adsSnap.docs.map(d => ({ id: d.id, ...d.data() }))
  const vehicles = vehiclesSnap.docs.map(d => ({ id: d.id, ...d.data() }))

  return {
    type: 'daily',
    date: dateStr,
    generatedAt: new Date().toISOString(),
    kpis: {
      totalAds: ads.length,
      activeAds: ads.filter(a => a.status === 'Active').length,
      totalVehicles: vehicles.length,
      activeVehicles: vehicles.filter(v => v.status === 'Active').length,
      totalImpressions: ads.reduce((s, a) => s + (a.impressions || 0), 0),
    },
    topAds: ads.filter(a => a.status === 'Active').sort((a, b) => (b.impressions || 0) - (a.impressions || 0)).slice(0, 5).map(a => ({ title: a.title, company: a.company, impressions: a.impressions || 0 })),
    vehicleSummary: {
      online: vehicles.filter(v => { if (!v.lastHeartbeat) return false; const d = v.lastHeartbeat.toDate ? v.lastHeartbeat.toDate() : new Date(v.lastHeartbeat); return (new Date() - d) / 1000 < 300 }).length,
      total: vehicles.length,
    },
  }
}

// Generate weekly report
export async function generateWeeklyReport(startDate, endDate) {
  const daily = await generateDailyReport()
  return {
    type: 'weekly',
    startDate: startDate?.toISOString?.() || new Date().toISOString(),
    endDate: endDate?.toISOString?.() || new Date().toISOString(),
    generatedAt: new Date().toISOString(),
    ...daily,
  }
}

// Generate monthly report
export async function generateMonthlyReport(month, year) {
  const daily = await generateDailyReport()
  return {
    type: 'monthly',
    month,
    year,
    generatedAt: new Date().toISOString(),
    ...daily,
  }
}

